/**
 * Récupère la signification courte d'une carte
 * @param {string} cardKey - Clé de la carte (translationKey)
 * @param {string} orientation - 'upright' ou 'reversed'
 * @param {string} language - Langue ('fr' ou 'en')
 * @returns {string|null} Signification courte ou null
 */
export function getCardMeaning(cardKey, orientation, language = 'fr') {
  const meanings = CARD_MEANINGS[language];
  if (!meanings) return null;
  
  const cardMeanings = meanings[cardKey];
  if (!cardMeanings) return null;
  
  return cardMeanings[orientation] || null;
}

export default CARD_MEANINGS;

